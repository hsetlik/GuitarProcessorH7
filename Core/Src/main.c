void checkLEDs() {
	static uint32_t lastCheck = 0;
	// this limits the LEDs to refreshing no more than ~25x a second
	const uint32_t minUpdateMs = 40;
	if (ledsReady && (SysTick->VAL - lastCheck) >= minUpdateMs) {
		lastCheck = SysTick->VAL;
		uint8_t bits = fx_get_led_byte(fx);
		if (bits != ledData) {
			ledData = bits;
			// even with the prescaler at 128 this transmission only takes like 10.2 microseconds,
			// but we have the hardware DMAs so why not optimize
			HAL_SPI_Transmit_DMA(&hspi2, &ledData, 1);
			ledsReady = 0;
		}
	}
}

const uint32_t switchUpdateMs = 10;

uint8_t algSwitchDebounce() {
	static uint16_t switchStates = 0;
	switchStates = (switchStates << 1)
			| HAL_GPIO_ReadPin(MODE_GPIO_Port, MODE_Pin);
	return switchStates == 0x000F;
}

void checkSwitches() {
	static uint32_t lastSwitchCheck = 0;
	uint32_t now = SysTick->VAL;
	static GPIO_PinState fxActive = GPIO_PIN_SET;
	if (now - lastSwitchCheck >= switchUpdateMs) {
		lastSwitchCheck = now;
		if (algSwitchDebounce()) {
			fx_advance_alg(fx);
		}
		GPIO_PinState bypState = HAL_GPIO_ReadPin(BYP_GPIO_Port, BYP_Pin);
		if (bypState != fxActive) {
			fxActive = bypState;
			fx_set_bypass(fx, (uint8_t) bypState);
		}
	}

}

// timer stuff
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *tim) {
	if (tim == &htim4) {
		if (HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t*) knobAdcData,
				sizeof(knobAdcData)) != HAL_OK) {
			Error_Handler();
		}
	} else if (tim == &htim3) {
		fx_update_display(fx);
		ssd1306_UpdateScren();
	}
}

