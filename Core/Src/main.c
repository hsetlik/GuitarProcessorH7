int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_I2C2_Init();
  MX_I2S1_Init();
  MX_SPI2_Init();
  MX_TIM4_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
	// configure codec over I2C
	if (TLV_quickInit_monoGuitarPedal() != HAL_OK) {
		Error_Handler();
	}
	//Start DMA transmission
	// note: buffer size is *2 here bc our 32 bit codec will use 2 16 bit frames
	HAL_StatusTypeDef status = HAL_I2SEx_TransmitReceive_DMA(&hi2s1,
			(uint16_t*) dacPtr, (uint16_t*) adcPtr, BUFFER_SIZE);
	if (status != HAL_OK) {
		Error_Handler();
	}
	// set up processor
	fx = create_fx_processor();

	// calibrate and start DMA for the control knob ADC
	if (HAL_ADCEx_Calibration_Start(&hadc1, ADC_CALIB_OFFSET, ADC_SINGLE_ENDED)
			!= HAL_OK) {
		Error_Handler();
	}

	if (HAL_TIM_Base_Start(&htim4) != HAL_OK) {
		Error_Handler();
	}

	if (HAL_TIM_Base_Start(&htim3) != HAL_OK) {
		Error_Handler();
	}

	// bring the NRST pin for the LEDs high
	HAL_GPIO_WritePin(LED_NRST_GPIO_Port, LED_NRST_Pin, GPIO_PIN_SET);

	// initialize the OLED display
	ssd1306_Init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1) {
		// check if we have the next buffer to process
		if (bufferReady) {
			convertInputBuffer();
			process_fx(fx, BUFFER_FLOAT_SIZE, inputFloatBuf, outputFloatBuf);
			convertOutputBuffer();
			bufferReady = 0;
		}
		// check the LEDs
		checkLEDs();
		checkSwitches();
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

